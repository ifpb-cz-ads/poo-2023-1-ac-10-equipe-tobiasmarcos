public class ConversaoDeUnidadesDeArea {
    public static void metrosQuadradosParaPesQuadrados(double metrosQuadrados) {
        System.out.println(metrosQuadrados * 10.76);
    }

    public static void pesQuadradosParaCentimetrosQuadrados(double pesQuadrados) {
        System.out.println(pesQuadrados * 929);
    }

    public static void milhasQuadradasParaAcres(double milhasQuadradas) {
        System.out.println(milhasQuadradas * 640);
    }

    public static void acresParaPesQuadrados(double acres) {
        System.out.println(acres * 43560);
    }
}
